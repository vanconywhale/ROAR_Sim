def location_is_same(loc, x, y, z):
    if (loc.x - x)**2 + (loc.y - y)**2 + (loc.z - z)**2 < 0.01:
        print("Got a hit: ", loc)
        return True
    else:
        print("not a hit")
        return False


def game_loop(args):
    pygame.init()
    pygame.font.init()
    world = None

    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(2.0)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)

        hud = HUD(args.width, args.height)
        world = World(client.get_world(), hud, args)
        controller = KeyboardControl(world, args.autopilot)
        # Get the information of current vehicle
        all_vehicles = world.world.get_actors().filter('vehicle.*')
        if len(all_vehicles) == 0:
            print("ERROR:  Failed to add new vehicle to the simulator, please close the server (UE4) and try again")
            return
        vehicles = [x for x in all_vehicles if x.id == world.player.id]
        this_vehicle = vehicles[0]

        clock = pygame.time.Clock()
        frame_id = 0
        while True:
            clock.tick_busy_loop(120) # Please feel free to set this FPS limitation to a higher or lower number.
            # read one event from the global event and provide it to the pygame.
            # if len(event_queue) != 0 and location_is_same(this_vehicle.get_location(), event_queue[0][0], event_queue[0][1], event_queue[0][2]): # when achieved the current frame
            if len(event_queue) != 0:
                tranform_data = event_queue.pop(0)
                loc = carla.Location(tranform_data[0], tranform_data[1], tranform_data[2])
                rot = carla.Rotation(tranform_data[3], tranform_data[4], tranform_data[5])
                velocity = carla.Vector3D(tranform_data[6], tranform_data[7], tranform_data[8])
                # set the status and action of current frame
                this_vehicle.set_transform(carla.Transform(loc, rot))
                this_vehicle.set_target_velocity(velocity)

                if frame_id % 200 == 0:
                    print('\r \nAll Vehicle Infos:\n' + json.dumps(world.get_all_vehicle_infos(), indent=4), flush=True)
                    print('\r \nAll Vehicle Ids:\n' + json.dumps(world.get_all_vehicle_ids(), indent=4), flush=True)
                    print('\r \nCurrent Infos:\n' + json.dumps(world.get_current_vehicle_info(), indent=4), flush=True)
                    print('\r \nCurrent Infos (Get By Id):\n' + json.dumps(world.get_vehicle_info_by_id(this_vehicle.id), indent=4), flush=True)

                # # update the pressed keys
                # if event_data[3] == pygame.KEYDOWN:
                #     if event_data[4] == K_UP:
                #         pressed_keys[K_UP] = True
                #     if event_data[4] == K_DOWN:
                #         pressed_keys[K_DOWN] = True
                #     if event_data[4] == K_LEFT:
                #         pressed_keys[K_LEFT] = True
                #     if event_data[4] == K_RIGHT:
                #         pressed_keys[K_RIGHT] = True
                #
                # if event_data[3] == pygame.KEYUP:
                #     if event_data[4] == K_UP:
                #         pressed_keys[K_UP] = False
                #     if event_data[4] == K_DOWN:
                #         pressed_keys[K_DOWN] = False
                #     if event_data[4] == K_LEFT:
                #         pressed_keys[K_LEFT] = False
                #     if event_data[4] == K_RIGHT:
                #         pressed_keys[K_RIGHT] = False
            frame_id += 1
            if controller.parse_events(client, world, clock):
                return
            world.tick(clock)
            world.render(display)
            pygame.display.flip()

    finally:

        if (world and world.recording_enabled):
            client.stop_recorder()

        if world is not None:
            world.destroy()

        pygame.quit()


